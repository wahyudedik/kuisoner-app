    <script src="https://cdn.jsdelivr.net/npm/chart.js@4.4.0/dist/chart.umd.min.js"></script>
    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // Readiness Distribution Pie Chart
            const readinessCtx = document.getElementById('readinessChart');
            if (readinessCtx && typeof Chart !== 'undefined') {
                const readinessData = @json($readinessData);
                const hasData = readinessData.data && readinessData.data.some(val => val > 0);
                
                if (hasData) {
                    new Chart(readinessCtx, {
                        type: 'pie',
                        data: {
                            labels: readinessData.labels,
                            datasets: [{
                                data: readinessData.data,
                                backgroundColor: readinessData.colors,
                                borderWidth: 2,
                                borderColor: '#fff'
                            }]
                        },
                        options: {
                            responsive: true,
                            maintainAspectRatio: true,
                            plugins: {
                                legend: {
                                    position: 'bottom',
                                },
                                tooltip: {
                                    callbacks: {
                                        label: function(context) {
                                            let label = context.label || '';
                                            if (label) {
                                                label += ': ';
                                            }
                                            label += context.parsed + ' responden';
                                            return label;
                                        }
                                    }
                                }
                            }
                        }
                    });
                }
            }

            // Average Score Per Aspect Bar Chart
            const aspectCtx = document.getElementById('aspectChart');
            if (aspectCtx && typeof Chart !== 'undefined') {
                const aspectData = @json($aspectData);
                const hasData = aspectData.data && aspectData.data.some(val => val > 0);
                
                if (hasData) {
                    new Chart(aspectCtx, {
                        type: 'bar',
                        data: {
                            labels: aspectData.labels,
                            datasets: [{
                                label: 'Rata-rata Persentase (%)',
                                data: aspectData.data,
                                backgroundColor: aspectData.color,
                                borderColor: aspectData.color,
                                borderWidth: 1
                            }]
                        },
                        options: {
                            responsive: true,
                            maintainAspectRatio: true,
                            scales: {
                                y: {
                                    beginAtZero: true,
                                    max: 100,
                                    ticks: {
                                        callback: function(value) {
                                            return value + '%';
                                        }
                                    }
                                }
                            },
                            plugins: {
                                legend: {
                                    display: false
                                },
                                tooltip: {
                                    callbacks: {
                                        label: function(context) {
                                            return 'Rata-rata: ' + context.parsed.y.toFixed(2) + '%';
                                        }
                                    }
                                }
                            }
                        }
                    });
                }
            }
        });
    </script>
